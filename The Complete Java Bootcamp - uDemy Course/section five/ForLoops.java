/**
 * ForLoops
 * 
 * Use a for loop when you KNOW how many times a code needs to run. 
 * The for loop runs code a specific number of times. 
 * 
 * Use a while loop when it's not clear how many times a code needs to run. 
 * The while loop runs code as long as something is true.
 */
public class ForLoops {
    public static void main(String[] args) {
        for (int i = 1; i <= 3; i++) {
            System.out.println(i);
        }
    }
}